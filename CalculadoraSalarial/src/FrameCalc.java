    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
      
       //CHAMAR MÉTODO,EXECUÇÃOO 
      if ( verificarVazio()== false){
      return;
      
      }
       
      if ( verificarNumeros()== false){
      return;
     }  
      
      double valorHora,qtdHora;
      
      valorHora = Double.parseDouble(txtValorHora.getText());
       qtdHora = Double.parseDouble(txtHorasTrab.getText());
      
       //OBJETO SENDO INSTANCIADO
     CalcSalarial calcSalarial = new CalcSalarial(valorHora,qtdHora);
     
     //OPÇÃO DE DEPENDENTES ATIVA OU NÃO
     if(rbDepSim.isSelected()){
         //
         int depSelec = cmbDep.getSelectedIndex();
         
        switch (depSelec){
            case 0:
                 depSelec = 1;
                break;
            case 1:
                depSelec = 2;
                break;
            case 2:
                depSelec = 3;
                break;
            case 3:
                depSelec = 4;
                break;
                
                 
        }
      //OBJETO(calcSalarial) + MÉTODO(AcrescentarDependentes) + VARIÁVEL QUE GUARDA VALOR DA CONVERSAO DO COMBO-BOX(depSelec) !!
      calcSalarial.AcrescentarDependentes(depSelec);
     } 
     if (cbxVA.isSelected()){
         calcSalarial.acrescentarVA();
     
     }
     if (cbxVT.isSelected()){
         calcSalarial.descontarVT();
     
     }
      if (cbxVR.isSelected()){
         calcSalarial.descontarVR();
     
     }
      if (cbxConv.isSelected()){
         calcSalarial.descontarCONV();
     
     }
     
     
     lblResp.setText ("<html>" +
                     "Salário Base: " + calcSalarial.getSalarioBase() +
                     "<br>Salário Líquido: " + calcSalarial.getSalarioLiquido() +
                     "<br>Total de descontos: " + calcSalarial.getTotalDescontos() +
                     "<br>Total de acrescimos: " + calcSalarial.getTotalAcrescimos() + 
                       "</html>");
    }//GEN-LAST:event_btnCalcularActionPerformed

