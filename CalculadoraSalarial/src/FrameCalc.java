    public boolean verificarNumeros(){
    
    String valorHoraStr,horasTrabStr;
      
    //CAPTURA DOS VALORES
    
      valorHoraStr = txtValorHora.getText();
     
      horasTrabStr = txtHorasTrab.getText();
     
      //conversao de String para double 
      double valorHora,horasTrab;
     try{
      
      valorHora = Double.parseDouble(valorHoraStr);
      //horasTrab = Double.parseDouble(horasTrabStr);
      } catch(Exception ex){
        JOptionPane.showMessageDialog(null,"Erro ao converter o valor por hora trabalhada!" + ex.getMessage());  
        txtValorHora.setText("");
       
        return false;
      }
     
     try{
      
      horasTrab = Double.parseDouble(horasTrabStr);
      //horasTrab = Double.parseDouble(horasTrabStr);
      } catch(Exception ex){
        JOptionPane.showMessageDialog(null,"Erro ao converter o valor das horas trabalhadas!" + ex.getMessage());  
        txtHorasTrab.setText("");
      
        return false;
        
      }
        return true;
     
    }
    
    //DECLARAÇÃO DO MÉTODO 
    public boolean verificarVazio (){ 
    
      //VARIAVEL PARA GUARDAR VALOR DOS TEXT FIELDS
      
      String valorHoraStr,horasTrabStr;
      
    //CAPTURA DOS VALORES
      valorHoraStr = txtValorHora.getText();
      horasTrabStr = txtHorasTrab.getText();
      
      //VERIFICA SE OS CAMPOS ESTÃO COM ESPAÇOS
      if (valorHoraStr.trim().length() == 0){
          JOptionPane.showMessageDialog(null,"Forneça o valor por hora trabalhada !");
          txtValorHora.requestFocus();
          
          return false;
      
      }
      
      if (horasTrabStr.trim().length() == 0){
          JOptionPane.showMessageDialog(null,"Forneça o valor das horas trabalhadas !");
          txtHorasTrab.requestFocus();
      
          
          return false;
      
      }
          return true;
    
    }
    
